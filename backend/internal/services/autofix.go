// --- GitLab Fix MR Creation ---

func (s *AutoFixService) createGitLabFixMR(project *models.Project, reviewLog *models.ReviewLog, patches []FilePatch, branchName, mrTitle string) (*FixResult, error) {
	projectPath := s.parseGitLabProject(project.URL)
	if projectPath == "" {
		return nil, fmt.Errorf("failed to parse GitLab project from URL: %s", project.URL)
	}

	baseURL := s.getGitLabBaseURL(project.URL)
	encodedPath := url.PathEscape(projectPath)

	// 1. Create branch
	branchPayload := map[string]string{
		"branch": branchName,
		"ref":    reviewLog.CommitHash,
	}
	if err := s.gitlabPost(fmt.Sprintf("%s/api/v4/projects/%s/repository/branches", baseURL, encodedPath), project.AccessToken, branchPayload, nil); err != nil {
		return nil, fmt.Errorf("failed to create branch: %w", err)
	}

	// 2. Commit files using the commits API
	var actions []map[string]string
	for _, patch := range patches {
		actions = append(actions, map[string]string{
			"action":    "update",
			"file_path": patch.FilePath,
			"content":   patch.Content,
		})
	}

	commitPayload := map[string]interface{}{
		"branch":         branchName,
		"commit_message": fmt.Sprintf("[CodeSentry] Auto-fix for review #%d", reviewLog.ID),
		"actions":        actions,
	}
	if err := s.gitlabPost(fmt.Sprintf("%s/api/v4/projects/%s/repository/commits", baseURL, encodedPath), project.AccessToken, commitPayload, nil); err != nil {
		return nil, fmt.Errorf("failed to commit fixes: %w", err)
	}

	// 3. Create MR
	targetBranch := reviewLog.Branch
	if targetBranch == "" {
		targetBranch = "main"
	}
	mrPayload := map[string]interface{}{
		"source_branch": branchName,
		"target_branch": targetBranch,
		"title":         mrTitle,
		"description":   fmt.Sprintf("Auto-generated fix by CodeSentry AI for review #%d.\n\nOriginal review score: %.0f/100\n\n---\n*This MR was automatically generated by [CodeSentry](https://github.com/huangang/codesentry)*", reviewLog.ID, safeScore(reviewLog.Score)),
	}

	var mrResp struct {
		WebURL string `json:"web_url"`
	}
	if err := s.gitlabPost(fmt.Sprintf("%s/api/v4/projects/%s/merge_requests", baseURL, encodedPath), project.AccessToken, mrPayload, &mrResp); err != nil {
		return nil, fmt.Errorf("failed to create MR: %w", err)
	}

	return &FixResult{PRURL: mrResp.WebURL, PRTitle: mrTitle}, nil
}

func (s *AutoFixService) parseGitLabProject(projectURL string) string {
	u := strings.TrimSuffix(projectURL, ".git")
	protocolIdx := strings.Index(u, "://")
	if protocolIdx == -1 {
		return ""
	}
	rest := u[protocolIdx+3:]
	slashIdx := strings.Index(rest, "/")
	if slashIdx == -1 {
		return ""
	}
	return rest[slashIdx+1:]
}

func (s *AutoFixService) getGitLabBaseURL(projectURL string) string {
	u := strings.TrimSuffix(projectURL, ".git")
	protocolIdx := strings.Index(u, "://")
	if protocolIdx == -1 {
		return "https://gitlab.com"
	}
	rest := u[protocolIdx+3:]
	slashIdx := strings.Index(rest, "/")
	if slashIdx == -1 {
		return u
	}
	return u[:protocolIdx+3+slashIdx]
}

func (s *AutoFixService) gitlabPost(apiURL, token string, payload interface{}, result interface{}) error {
	body, _ := json.Marshal(payload)
	req, _ := http.NewRequest("POST", apiURL, bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("PRIVATE-TOKEN", token)
	resp, err := s.httpClient.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()
	if resp.StatusCode >= 400 {
		respBody, _ := io.ReadAll(resp.Body)
		return fmt.Errorf("gitlab API error (status %d): %s", resp.StatusCode, string(respBody))
	}
	if result != nil {
		json.NewDecoder(resp.Body).Decode(result)
	}
	return nil
}

// --- Helpers ---

func encodeBase64(s string) string {
	return base64.StdEncoding.EncodeToString([]byte(s))
}

func safeScore(score *float64) float64 {
	if score == nil {
		return 0
	}
	return *score
}
